// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 風船が割れる処理
// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void CBalloon::BreakBalloon(int const &nCntBalloon)
{
	// 当たり判定状態がtrueなら
	// ->開放
	if (m_apSceneX[nCntBalloon]->GetbCollision())
	{
		m_apSceneX[nCntBalloon]->Uninit();
		delete m_apSceneX[nCntBalloon];
		m_apSceneX[nCntBalloon] = NULL;
	}
}