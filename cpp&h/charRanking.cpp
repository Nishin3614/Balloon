// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//
// コンストラクタ
//
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
CCharRanking::CCharRanking() : CScene()
{
	// 初期化
	for (int nCntPlayer = 0; nCntPlayer < MAX_PLAYER; nCntPlayer++)
	{
		m_aCharaPhoto[nCntPlayer] = NULL;
	}
}

